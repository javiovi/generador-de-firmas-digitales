async function generateIcon(type: string, color: string, theme: 'light' | 'dark' = 'light'): Promise<Buffer> {
  const isContactIcon = contactIcons[type];
  const isSocialIcon = socialIcons[type];

  if (!isContactIcon && !isSocialIcon) {
    throw new Error(`Tipo de ícono no válido: ${type}`);
  }

  // Obtener el SVG del ícono
  let svg: string;
  if (isContactIcon) {
    // Para íconos de contacto, usar el color según el tema
    const iconColor = theme === 'light' ? '#000000' : '#FFFFFF';
    svg = contactIcons[type].replace('currentColor', iconColor);
  } else {
    // Para íconos sociales, usar el color proporcionado
    svg = socialIcons[type].replace('currentColor', color);
  }

  // Crear un canvas SVG con el ícono
  const svgWrapper = `
    <svg width="24" height="24" xmlns="http://www.w3.org/2000/svg">
      ${svg}
    </svg>
  `;

  // Convertir SVG a PNG usando Sharp
  return sharp(Buffer.from(svgWrapper))
    .png()
    .toBuffer();
}

export async function POST(request: NextRequest) {
  try {
    const { type, color, theme } = await request.json();

    if (!type) {
      return NextResponse.json({ error: "Type is required" }, { status: 400 });
    }

    const buffer = await generateIcon(type, color || '#4F46E5', theme || 'light');

    // Devolver el buffer como una imagen PNG
    return new NextResponse(buffer, {
      headers: {
        'Content-Type': 'image/png',
        'Cache-Control': 'public, max-age=31536000, immutable'
      }
    });
  } catch (error) {
    console.error("Error generating icon:", error);
    return NextResponse.json({ error: "Failed to generate icon" }, { status: 500 });
  }
}

export async function GET(request: NextRequest) {
  try {
    const searchParams = request.nextUrl.searchParams;
    const type = searchParams.get('type');
    const color = searchParams.get('color');
    const theme = searchParams.get('theme') as 'light' | 'dark' || 'light';

    if (!type) {
      return NextResponse.json({ error: "Type is required" }, { status: 400 });
    }

    const buffer = await generateIcon(type, color || '#4F46E5', theme);

    // Devolver el buffer como una imagen PNG
    return new NextResponse(buffer, {
      headers: {
        'Content-Type': 'image/png',
        'Cache-Control': 'public, max-age=31536000, immutable'
      }
    });
  } catch (error) {
    console.error("Error generating icon:", error);
    return NextResponse.json({ error: "Failed to generate icon" }, { status: 500 });
  }
}

